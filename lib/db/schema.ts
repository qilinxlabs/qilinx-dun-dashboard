import type { InferSelectModel } from "drizzle-orm";
import {
  boolean,
  foreignKey,
  integer,
  json,
  pgTable,
  primaryKey,
  text,
  timestamp,
  uuid,
  varchar,
} from "drizzle-orm/pg-core";

// MCP Tools Configuration stored per user
export interface MCPToolsConfig {
  disabledToolIds: string[]; // Array of mcp_tools.id that user has disabled
}

export const user = pgTable("User", {
  id: uuid("id").primaryKey().notNull().defaultRandom(),
  email: varchar("email", { length: 64 }), // Made optional for backward compatibility
  password: varchar("password", { length: 64 }), // Made optional for backward compatibility
  walletAddress: varchar("walletAddress", { length: 44 }).unique(), // Solana wallet address
  nonce: varchar("nonce", { length: 64 }), // For signature verification
  apiToken: uuid("apiToken").defaultRandom(),
  mcpToolsConfig: json("mcpToolsConfig").$type<MCPToolsConfig>(),
});

export type User = InferSelectModel<typeof user>;

// MCP Tools table for storing external tool server configurations
export const mcpTools = pgTable("MCP_Tools", {
  id: uuid("id").primaryKey().notNull().defaultRandom(),
  name: varchar("name", { length: 128 }).notNull(),
  description: text("description"),
  host: varchar("host", { length: 512 }).notNull(),
  isActive: boolean("isActive").notNull().default(true),
  createdAt: timestamp("createdAt").notNull().defaultNow(),
});

export type MCPTool = InferSelectModel<typeof mcpTools>;

export const chat = pgTable("Chat", {
  id: uuid("id").primaryKey().notNull().defaultRandom(),
  createdAt: timestamp("createdAt").notNull(),
  title: text("title").notNull(),
  userId: uuid("userId")
    .notNull()
    .references(() => user.id),
  visibility: varchar("visibility", { enum: ["public", "private"] })
    .notNull()
    .default("private"),
});

export type Chat = InferSelectModel<typeof chat>;

